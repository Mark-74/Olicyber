NOTES = "ccggaag-ffeeddc-ggffeed-ggffeed-ccggaag-ffeeddc-"

key = [0x2a, 0x39, 0x67, 0x7e, 0x71, 0x2d, 0x21, 0x21, 0x72, 0x43, 0x41, 0x3a, 0x09, 0x0c, 0x04, 0x05, 0x52, 0x42, 0x40, 0x19, 0x0a, 0x77, 0x51, 0x42, 0x05, 0x23, 0x36, 0x5b, 0x0c, 0x67, 0x66, 0x22, 0x00, 0x4b, 0x27, 0x38, 0x47, 0x72, 0x6e, 0x5b, 0x75, 0x2b, 0x2c, 0x32, 0x5e, 0x53, 0x47, 0x5a, 0x22]

loc_461 = 0x2a
loc_464 = 0
FLAG = ""

for note in NOTES:
    loc_464 = (loc_464 + 1) % 0x31
    key_idx = loc_464
    
    tmp = loc_461 ^ ord(note) ^ key[key_idx]
    xored = tmp & 0xff
    loc_461 = xored
    FLAG += chr(xored)

print(NOTES)
print(FLAG)
